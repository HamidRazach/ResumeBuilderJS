export const getDaysUntilDeadline =(deadline)=> {
  // Get the current date
  const today = new Date();
    
  // Parse the deadline date (ensure the format is YYYY-MM-DD)
  const deadlineDate = new Date(deadline);
  
  // Calculate the difference in time (in milliseconds)
  const timeDifference = deadlineDate - today;

  
  // Convert the time difference to days (ignoring time portion)
  const daysDifference = Math.floor(timeDifference / (1000 * 60 * 60 * 24));

  // Return the number of days left
  return daysDifference;
}

export const checkDeviceAndBrowser = () => {
  const userAgent = navigator.userAgent.toLowerCase();

  // Check if the device is iPhone
  const isIOS = /iphone|ipad|ipod/.test(userAgent) && !window.MSStream;

  // Check if the browser is Safari
  const isSafari = userAgent.includes("safari") && !userAgent.includes("chrome");

  // If all conditions are true, return true, otherwise return false
  if (isIOS && isSafari) {
    return true;
  }

  return false;
};
