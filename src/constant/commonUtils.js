export function truncateText(text, maxWidth, maxLines, font = "16px Arial") {
  // Create a canvas element to measure text width
  const canvas = document.createElement("canvas");
  const context = canvas.getContext("2d");
  context.font = font;

  // Helper function to calculate the width of the text
  const getTextWidth = (txt) => context.measureText(txt).width;

  let truncatedText = "";
  let lineCount = 0;
  let currentLine = "";

  // Iterate over each word
  for (let word of text.split(" ")) {
    let newLine = currentLine + (currentLine.length > 0 ? " " : "") + word;
    let lineWidth = getTextWidth(newLine);

    // Check if the line exceeds the maximum width
    if (lineWidth > maxWidth) {
      lineCount++;
      // Append the current line to the truncated text
      truncatedText += currentLine + "\n";

      // If we have reached the maxLines limit, add ellipsis
      if (lineCount >= maxLines) {
        truncatedText = truncatedText.trim() + "...";
        break;
      }
      // Start a new line
      currentLine = word;
    } else {
      currentLine = newLine;
    }
  }

  // Add the last line if it's within the allowed lines
  if (lineCount < maxLines) {
    truncatedText += currentLine;
  }

  return truncatedText.trim();
}

