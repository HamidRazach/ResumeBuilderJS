export const formatString = (str) => {
  if (!str) return "";

  // Case 1: Check if it starts with $ and has a range with '-'
  if (str.startsWith("$") && str.includes("-")) {
    const rangeParts = str.split("-");
    if (rangeParts.length === 2) {
      const formattedRange = rangeParts.map((part) =>
        formatPriceToK(part.trim())
      );
      return `${formattedRange[0]} - ${formattedRange[1]}`;
    }
  }

  // Case 2: If it starts with $ but has no '-'
  if (str.startsWith("$")) {
    return formatPriceToK(str);
  }

  // Case 3: If it doesn't contain '$', return with a limit of 10 characters
  if (!str.includes("$")) {
    return str.length > 4 ? `${str.slice(0, 4)}...` : str;
  }

  return str;
};

// Helper function to convert price to K notation
export const formatPriceToK = (price) => {
  const numericPrice = Number(price.replace(/[^0-9.-]+/g, "")); // Remove non-numeric characters like "$" and ","

  if (isNaN(numericPrice)) {
    return "Invalid price"; // Handle invalid inputs
  }

  if (numericPrice >= 1000) {
    return `$${numericPrice / 1000}K`; // Format in 'K'
  }

  return `$${numericPrice}`; // Return the original value if it's less than 1000
};

export const formatDeadline = (date) => {
  let deadline = ""; // Initialize deadline as an empty string

  // Check if the input is in the format 'Mar-25' (month abbreviation and year)
  const monthYearRegex = /^[a-zA-Z]{3}-\d{2}$/;

  if (typeof date === "string" && monthYearRegex.test(date)) {
    // Handle 'Mar-25' format
    const [monthStr, yearStr] = date.split("-");

    // Convert the month string to a corresponding month number (0-indexed for JavaScript Date object)
    const monthIndex = new Date(`${monthStr} 1, 2023`).getMonth(); // Using a dummy year to get month

    const year = parseInt(`20${yearStr}`); // Interpret the year as 20XX (e.g., '25' -> '2025')

    // Set the day as 1
    date = new Date(year, monthIndex, 1);
  }

  // Check if the date is in the format 'YYYY-MM-DD'
  if (typeof date === "string" && /^\d{4}-\d{2}-\d{2}$/.test(date)) {
    date = new Date(date);
  }

  // Check if input is a valid Date object
  if (!(date instanceof Date) || isNaN(date.getTime())) {
    return ""; // Return an empty string if the date is invalid
  }

  // Format the date as 'YYYY-MM-DD'
  const year = date.getFullYear();
  const month = ("0" + (date.getMonth() + 1)).slice(-2); // Ensure month is two digits
  const day = ("0" + date.getDate()).slice(-2); // Ensure day is two digits

  return `${year}-${month}-${day}`;
};

